    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        
        int baris = tabel.getSelectedRow();
        if(baris != 1)
        {
            if("Nasi Ayam".equals(tabel.getValueAt(baris, 0).toString()))
            {
                NA.setSelected(true);
            }
            else if ("Nasi Goreng".equals(tabel.getValueAt(baris, 0).toString()))
            {
                NG.setSelected(true);
            }
            else if ("Mie Ayam".equals(tabel.getValueAt(baris, 0).toString()))
            {
                MA.setSelected(true);
            }
            else
            {
                R.setSelected(true);
            }
            
            if("Es Teh".equals(tabel.getValueAt(baris, 1).toString()))
            {
                teh.setSelected(true);
            }
            else if ("Es Jeruk".equals(tabel.getValueAt(baris, 1).toString()))
            {
               jeruk.setSelected(true);
            }
            else if ("Es Teler".equals(tabel.getValueAt(baris, 1).toString()))
            {
                teler.setSelected(true);
            }
            else
            {
               air.setSelected(true);
            }
            
        }
    }//GEN-LAST:event_tabelMouseClicked

