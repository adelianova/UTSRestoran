    private void selectData() {
        String kolom[]={"Makanan","Jumlah Makanan","Minuman","Jumlah Minuman"};
       DefaultTableModel dtm = new DefaultTableModel(null, kolom);
       String SQL = "SELECT * FROM tb_resto";
       ResultSet rs = KoneksiDB.executeQuery(SQL);
       try{
           while(rs.next())
           {
               String makan = rs.getString(1);
                String jml_mkn= rs.getString(2);
                String minum = rs.getString(3);
                String jml_minum = rs.getString(4);
//                String total = rs.getString(5);
                String data[] = {makan,jml_mkn,minum,jml_minum};
                dtm.addRow(data);
           }
       } catch(SQLException ex)
       {
           //Logger.getLogger(FormBiodata.class.getName()).Log(Level.SEVERE, null, ex);
       }
       tabel.setModel(dtm);
    }
